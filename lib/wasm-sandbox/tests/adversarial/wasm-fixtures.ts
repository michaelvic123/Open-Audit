/**
 * wasm-fixtures.ts
 *
 * Adversarial WASM module bytecodes for the security test suite.
 *
 * These are hand-crafted minimal WASM binaries that exercise specific
 * sandbox boundary conditions. They are committed as byte arrays so the
 * test suite runs in CI without a Rust/AssemblyScript toolchain.
 *
 * Each module exports the minimal interface expected by WasmSandboxRunner:
 *   - alloc(size: i32) -> i32
 *   - dealloc(ptr: i32) -> void
 *   - parse(ptr: i32, len: i32) -> i32
 *
 * Adversarial cases:
 *   1. MEMORY_BOMB    — attempts to grow memory beyond 16MB (256 pages)
 *   2. INFINITE_LOOP  — busy-loops past the 5s timeout
 *   3. HOST_PROBE     — attempts to import filesystem/network host functions
 *   4. MALFORMED_OUTPUT — parse() returns a pointer to non-JSON bytes
 */

/**
 * Case 1: Memory bomb.
 * Attempts to call memory.grow with a value exceeding MAX_MEMORY_PAGES (256).
 * WASM memory.grow returns -1 when growth is denied by the engine.
 * The sandbox must catch this and reject/terminate the module.
 *
 * WAT equivalent:
 *   (module
 *     (memory (export "memory") 1 256)
 *     (func (export "alloc") (param i32) (result i32)
 *       ;; attempt to grow beyond limit
 *       i32.const 512
 *       memory.grow
 *       drop
 *       i32.const 0)
 *     (func (export "dealloc") (param i32))
 *     (func (export "parse") (param i32 i32) (result i32)
 *       i32.const 512
 *       memory.grow
 *       drop
 *       i32.const 0))
 */
export const MEMORY_BOMB_WASM = new Uint8Array([
  0x00, 0x61, 0x73, 0x6d, // magic: \0asm
  0x01, 0x00, 0x00, 0x00, // version: 1
  // Type section: 3 function types
  0x01, 0x10,
  0x03,
  0x60, 0x01, 0x7f, 0x01, 0x7f, // (i32) -> i32
  0x60, 0x01, 0x7f, 0x00,        // (i32) -> void
  0x60, 0x02, 0x7f, 0x7f, 0x01, 0x7f, // (i32,i32) -> i32
  // Function section
  0x03, 0x04, 0x03, 0x00, 0x01, 0x02,
  // Memory section: min=1, max=256
  0x05, 0x04, 0x01, 0x01, 0x01, 0xff, 0x01,
  // Export section
  0x07, 0x1c,
  0x04,
  0x06, 0x6d, 0x65, 0x6d, 0x6f, 0x72, 0x79, 0x02, 0x00,
  0x05, 0x61, 0x6c, 0x6c, 0x6f, 0x63, 0x00, 0x00,
  0x07, 0x64, 0x65, 0x61, 0x6c, 0x6c, 0x6f, 0x63, 0x00, 0x01,
  0x05, 0x70, 0x61, 0x72, 0x73, 0x65, 0x00, 0x02,
  // Code section
  0x0a, 0x15, 0x03,
  // alloc: memory.grow(512) drop; return 0
  0x08, 0x00, 0x41, 0x80, 0x04, 0x40, 0x00, 0x1a, 0x41, 0x00, 0x0b,
  // dealloc: nop
  0x02, 0x00, 0x0b,
  // parse: memory.grow(512) drop; return 0
  0x08, 0x00, 0x41, 0x80, 0x04, 0x40, 0x00, 0x1a, 0x41, 0x00, 0x0b,
]);

